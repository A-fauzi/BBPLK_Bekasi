fun main() {

    println("While loops")
    var number = 0
    while (number < 10) {
        number++
        println("Hello World urutan loop ke $number")
    }
    println()

    println("Do While")
    var anotherNumber = 0
    do {
        anotherNumber++
        println("Hello World urutan loop ke $anotherNumber")
    }while (
        anotherNumber < 10
    )
    println("break")

}